    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtNameCategory.getText().trim().equals("")) {
            Messages.showMessageDialog("Ingrese nombre de la categoria", "ADVERTENCIA!");
            txtNameCategory.requestFocus();
            return;
        }
        if (objAddOp) {
            //Agregar
            addCategory();

        } else {
            //Editar
            editCategory();
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        objAddOp = false;
        stateEdit();
        txtIdCategory.requestFocus();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String id = txtIdCategory.getText().trim();
        if (id.equals("")) {
            Messages.showMessageDialog("Busque la categoria a Eliminar", "ADVERTENCIA!!");
            txtIdCategory.requestFocus();
            return;
        }
        Long categoryId = Long.parseLong(id);
        if (Messages.showConfirmDialog("Desea eliminar esta categoria?", "CONFIRME!") == JOptionPane.YES_NO_OPTION) {
            if (objFService.category.deleteCategory(categoryId)) {
                Messages.showMessageDialog("Categoria eliminada correctamente", "ADVERTENCIA!");
                stateInitial();
                cleanControls();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    private void stateEdit() {
        btnNuevo.setVisible(false);
        btnEditar.setVisible(false);
        btnEliminar.setVisible(true);
        btnCancelar.setVisible(true);
        btnCerrar.setVisible(false);
        btnSave.setVisible(true);
        txtIdCategory.setEnabled(true);
        txtNameCategory.setEnabled(true);
    }

    private void stateInitial() {
        btnNuevo.setVisible(true);
        btnEditar.setVisible(true);
        btnEliminar.setVisible(false);
        btnCancelar.setVisible(false);
        btnCerrar.setVisible(true);
        btnSave.setVisible(false);
        txtIdCategory.setEnabled(false);
        txtNameCategory.setEnabled(false);
    }

