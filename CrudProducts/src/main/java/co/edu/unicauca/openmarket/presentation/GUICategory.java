    private void stateNew() {
        btnNuevo.setVisible(false);
        btnEditar.setVisible(false);
        btnEliminar.setVisible(false);
        btnCancelar.setVisible(true);
        btnCerrar.setVisible(false);
        btnSave.setVisible(true);
        txtIdCategory.setEnabled(false);
        txtNameCategory.setEnabled(true);
    }

    private void cleanControls() {
        txtIdCategory.setText("");
        txtNameCategory.setText("");
    }

    private void addCategory() {
        String name = txtNameCategory.getText().trim();

        if (objFService.category.saveCategory(name)) {
            Messages.showMessageDialog("Guardada Correctamente", "ADVERTENCIA");
            cleanControls();
            stateInitial();
        } else {
            Messages.showMessageDialog("Error al guardar!", "ADVERTENCIA");
        }
    }

    private void editCategory() {
        String id = txtIdCategory.getText().trim();
        if (id.equals("")) {
            Messages.showMessageDialog("Busque la categoria a editar", "ADVERTENCIA");
            txtIdCategory.requestFocus();
            return;
        }

        Long categoryId = Long.parseLong(id);
        Category category = new Category();
        category.setName(txtNameCategory.getText().trim());

        if (objFService.category.editCategory(categoryId, category)) {
            Messages.showMessageDialog("Edito Correctamente", "ADVERTENCIA");
            cleanControls();
            stateInitial();
        } else {
            Messages.showMessageDialog("Error al editar!", "ADVERTENCIA");
        }
    }
